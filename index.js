// Error Handler
bot.catch((err, ctx) => {
  console.error('Bot error:', {
    error: err,
    updateType: ctx.updateType,
    userData: ctx.from,
    messageText: ctx.message?.text
  });

  if (err.code === 'ETIMEDOUT') {
    ctx.reply('âŒ› Request timed out. Please try again.');
  } else if (err.response && err.response.status === 429) {
    ctx.reply('âš ï¸ Too many requests. Please try again later.');
  } else {
    ctx.reply('âŒ An error occurred. Please try again later.');
  }
});

// Start bot
bot.launch()
  .then(() => {
    console.log('ðŸ¤– Future Edge Bot is running');
    console.log('âœ… Token scanner is active on ETH and BSC chains');
    console.log('ðŸ“¡ Connected to EVA API');
  })
  .catch(err => {
    console.error('Bot failed to start:', err);
    process.exit(1);
  });

// Graceful shutdown
process.once('SIGINT', () => {
  console.log('Bot is shutting down...');
  bot.stop('SIGINT');
});

process.once('SIGTERM', () => {
  console.log('Bot is shutting down...');
  bot.stop('SIGTERM');
});

// Handle unhandled promise rejections
process.on('unhandledRejection', (reason, promise) => {
  console.error('Unhandled Rejection:', {
    reason: reason,
    promise: promise
  });
});

module.exports = bot;